		[Enfance inconnue]<br/>
		Elle fuit de chez elle déguisée en garçon et embarque sur le <i>Griffon</i>, acceptée par le Capitaine Shark pour son aplomb. Elle grandit en protégeant son secret jusqu'à ce qu'au cours d'un assaut, une grave blessure à la poitrine révèle sa féminité. L'équipage se divise instantanément, entre détracteurs supersticieux et ses soutiens, amis, personnes à qui elle a sauvé la vie. Shark tranche en décidant de la garder à bord et lui offre sa protection. Leur complicité réciproque se renforce, le vieux capitaine appréciant ce bout de femme au caractère bien trempé.<br/>
		
		À sa mort, il la désigne comme seule héritière du <i>Griffon</i> et de ses biens. Contrainte par ses difficultés à maintenir l'ordre  et les risques de mutinerie, elle est contrainte  à prendre comme second l'un des opposants (élection à la majorité). C'est en fait lui qui dirige, tout en mettant sur le dos de Sigrid les erreurs commises. Cependant, elle est maligne, stratégiquement douée et aidée par des amis puissants (*) ce qui lui permet d'éviter d'être dfinitivement déstituée.<br/>
		
		Un jour, elle prend le navire de Vatek mais l'épargne, attirée par son charisme, ses talents de bretteur et ses qualités de meneur d'hommes. Elle lui propose de le rejoindre au sein de son équipage, ce qui provoque une mutinerie jugulée par Vatek et ses hommes.<br/>
		
		Une grande complicité se noue entre eux et ils ne tardent pas à tomber amoureux. Sigrid le nomme alors Capitaine et ils deviennent les Amants de la Mer du Navire au Deux Capitaines, faisant régner leur loi sur la Mer du Milieu (des côtes westiennes aux côtes ystiennes). Les Marines royales placent sur leur tête une prime faramineuse, poussant nombre de chasseurs de prime à les poursuivre mais toujours en vain.<br/>

		Un jour, ils entendent parler d'un lourd et lent galion chargé d'or westien et se mettent en chasse. Malheureusement c'est un piège de l'ancien Second de Sigrid et ils sont tout deux abandonnés sur une île déserte pour y mourir pendant que le Second récupère le Griffon pour son propre compte. Des esclavagistes passant par là les capturent et ignorant leur valeur, les vendent sur le Marché aux Esclaves de Port-Royal.<br/>

		Sigrid est repérée par une maqueraude pour ses formes avantageuses et enrôlée de force dans une maison close où on la "forme". Rebelle, elle s'enfuit mais, attrapée aux portes de la ville, elle est fouettée et revendue comme domestique dans une grande maison bourgeoise de Chenia.<br/>

		Ses nouveaux propriétaires sont bien évidemment méfiants (cicatrices, tatouages, marque au fer rouge des esclaves et des fugueurs ...) et ne lui laisse aucune marge de manoeuvre. Mais elle finit par gagner la confiance de la fille du maître de maison à qui elle apprend à se battre. À la faveur d'un excès de confiance de celle-ci, elle s'échappe de jour et fuit vers le Nord, à la recherche de Vatek.<br/>

		Après des décades de voyages au sein d'une caravane de la Route des Neiges, elle franchit le col de l'Exa et cherche dans de nombreuses villes mais en vain. Brisée, épuisée, loin de chez elle dans une contrée froide et hostile, elle sombre dans l'alcool, la dépression et la débauche.<br/>

		Elle est trouvée par Owyn (ou un de ses collègues) qui détecte en elle un léger pouvoir et propose de la guider vers la guérison et la connaissance d'elle-même. Dans le même temps, il parcourt les esprits du Nord à la recherche de Vatek mais ne le trouve pas (impossible de percer ses défenses mentales) et en déduit sa mort. Seule et désespérée, Sigrid accepte de le suivre mais renonce bientôt en raison de ses trop faibles capacités et de ses désirs de haine et de violences qui se tournent peu à peu vers son ancien second. L'appel de la mer est trop fort, et sa rédemption s'incarne dans une vengeance implacable.<br/>

		Ils se séparent d'un commun accord et elle retourne à Port Réal. Par le jeux de ses anciennes alliances, dont certaines ne sont heureusement pas mortes, elle réussi à trouver un navire grâce à un vieil ami (*) qui lui doit une faveur. Elle récupère le <i>Griffon</i> dont elle massacre les officiers et le Capitaine puis traque son ancien Second auquel elle fait subir des jours de torture avant qu'il ne meure, de façon involontaire.<br/>

		Enfin rassasiée, elle fait peu à peu son deuil de Vatek et reprend ses activités, sans toutefois retrouvée l'intégralité de sa gloire passée. Grâce à l'or caché de Shark, elle embauche un nouvel équipage donc un jeune matelot un peu naïf et attachant. Doux, sensible, gentil, en un mot "bonne pâte", il devient son soutien sans faille et elle en tombe amoureuse avant de l'épouser à la façon des pirates.<br/>

		Elle recroisera Owyn et Vatek au cours de l'aventure, lorsque celui-ci recherche un navire pour fuir Ystia. Il sera un temps la tentation avant que tout deu ne comprennent que trop de temps s'est écoulé, qu'ils ne sont plus les mêmes et ne peuvent plus s'aimer comme avant. Ils resteront excellents amis, partageant le goût de la débauche et l'esprit des pirates.
